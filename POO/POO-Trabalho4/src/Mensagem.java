
public class Mensagem {

	private String textoMensagem;
	
	public Mensagem(String textoMensagem) { //metodo construtor
		
		this.textoMensagem = textoMensagem;
		
	}
	
	public void setTextoMensagem(String textoMensagem) { //set da mensagem
		
		this.textoMensagem = textoMensagem;
		
	}
	
	public String getTextoMensagem() { //get da mensagem
		
		return textoMensagem;
		
	}
	
}
